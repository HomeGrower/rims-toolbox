    <h3>Datastore Builder Guide</h3>
    
    <h4>Overview</h4>
    <p>The Datastore Builder allows you to configure which data tables are available for your project. Tables are automatically categorized based on their source:</p>
    
    <h4>Table Categories</h4>
    <ul>
        <li><strong>📦 Standard Tables</strong> - Basic tables available to all projects</li>
        <li><strong>🔒 Required Tables</strong> - Essential tables that cannot be disabled</li>
        <li><strong>🔧 Module Tables</strong> - Tables provided by your selected modules</li>
        <li><strong>🏢 Brand Tables</strong> - Custom tables specific to your hotel brand</li>
        <li><strong>🚫 Disabled Tables</strong> - Tables not currently active</li>
    </ul>
    
    <h4>Status Icons</h4>
    <ul>
        <li>✅ Active - Table is enabled and configurable</li>
        <li>❌ Disabled - Table is not available</li>
        <li>🔒 Required - Table cannot be disabled</li>
        <li>⭐ Custom - Brand or chain-specific table</li>
    </ul>
    
    <h4>How to Use</h4>
    <ol>
        <li><strong>Enable/Disable Tables</strong> - Click the toggle button to activate or deactivate tables</li>
        <li><strong>Configure Fields</strong> - Select a table in the middle column to edit its fields</li>
        <li><strong>Add Custom Fields</strong> - Use the field editor to add new fields to any active table</li>
        <li><strong>Import/Export</strong> - Save your configuration or load from a template</li>
    </ol>
    
    <h4>Tips</h4>
    <ul>
        <li>Required tables are always active and cannot be disabled</li>
        <li>Module-specific tables are only available when the module is selected for your project</li>
        <li>Changes are saved automatically when you click "Save Configuration"</li>
        <li>Use the JSON preview to see the exact configuration that will be generated</li>
    </ul>
</div>